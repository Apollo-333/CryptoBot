    """–û–±—Ä–∞–±–æ—Ç—á–∏–∫ —Ç–µ–∫—Å—Ç–æ–≤—ã—Ö —Å–æ–æ–±—â–µ–Ω–∏–π"""
    user = update.effective_user
    
    # –ï—Å–ª–∏ —ç—Ç–æ –∞–¥–º–∏–Ω –∏ –Ω–µ—Ç –∫–æ–º–∞–Ω–¥—ã - –∏–≥–Ω–æ—Ä–∏—Ä—É–µ–º
    if str(user.id) == str(ADMIN_ID) and ADMIN_ID != 0:
        if update.message.text and update.message.text.startswith('/'):
            return  # –ö–æ–º–∞–Ω–¥—ã –æ–±—Ä–∞–±–∞—Ç—ã–≤–∞—é—Ç—Å—è –æ—Ç–¥–µ–ª—å–Ω–æ
        # –ê–¥–º–∏–Ω –º–æ–∂–µ—Ç –ø—Ä–æ—Å—Ç–æ –ø–∏—Å–∞—Ç—å, —ç—Ç–æ –Ω–µ –ø–µ—Ä–µ—Å—ã–ª–∞–µ—Ç—Å—è
    
    # –ü–µ—Ä–µ—Å—ã–ª–∞–µ–º —Å–æ–æ–±—â–µ–Ω–∏—è –æ—Ç –æ–±—ã—á–Ω—ã—Ö –ø–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª–µ–π
    if update.message.text and not update.message.text.startswith('/'):
        await forward_to_admin(update, context)

